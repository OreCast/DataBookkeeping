SELECT
    D.DATASET,
    D.META_ID,
    S.SITE,
    PR.PROCESSING,
    P.PARENT,
    D.CREATE_BY,
    D.CREATION_DATE,
    D.LAST_MODIFIED_BY,
    D.LAST_MODIFICATION_DATE
FROM DATASETS D
JOIN SITES S on S.DATASET_ID=D.DATASET_ID
JOIN PARENTS P on P.DATASET_ID=D.DATASET_ID
JOIN PROCESSING PR on PR.DATASET_ID=D.DATASET_ID
