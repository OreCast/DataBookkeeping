SELECT
    D.DATASET,
    D.META_ID,
    S.SITE,
    PR.PROCESSING,
    P.PARENT,
    D.CREATE_BY,
    D.CREATION_DATE,
    D.LAST_MODIFIED_BY,
    D.LAST_MODIFICATION_DATE
FROM DATASETS D
JOIN SITES S on S.SITE_ID=D.SITE_ID
JOIN PROCESSING PR on PR.PROCESSING_ID=D.PROCESSING_ID
LEFT OUTER JOIN PARENTS P on P.PARENT_ID=D.PARENT_ID
